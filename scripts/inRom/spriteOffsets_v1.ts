const lines = input.split('\n');

const entries = lines.map((line) => {
	const splits = line.split(':');

	const offset = parseInt(splits[0].trim(), 16) + 2092;
	const name = splits[1]?.trim() ?? '';

	if (name) {
		return { offset, name };
	} else {
		return { offset };
	}
});

entries.forEach((e) => {
	let src = `{ offset: 0x${e.offset.toString(16)}`;

	if (e.name) {
		src += `, name: "${e.name}"`;
	}

	src += ' },';

	console.log(src);
});

